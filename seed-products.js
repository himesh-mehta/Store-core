async function seedProducts() {
  console.log(`=== Seeding ${sampleProducts.length} Products into Neon PostgreSQL ===`);
  try {
    // Clear existing and re-seed
    await sql`DELETE FROM products`;
    console.log('🗑  Cleared existing products.');

    for (const p of sampleProducts) {
      console.log(`  ↳ Inserting: "${p.name}" — ₹${p.price}`);
      await sql`
        INSERT INTO products (name, description, price, category, image_url, stock, rating)
        VALUES (${p.name}, ${p.description}, ${p.price}, ${p.category}, ${p.image_url}, ${p.stock}, ${p.rating})
      `;
    }
    console.log(`\n✅ Seeded ${sampleProducts.length} products successfully!`);
  } catch (err) {
    console.error('❌ Seeding failed:', err.message);
  }
}

